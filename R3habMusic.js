const titles = tracks.map((v, i)=>`${v.cleanTitle}~${i}`).join('|');

let what = context(()=> {
    follow(`(it's|the name is|) $(T~ ${titles})`,
        p => p.resolve(tracks[parseInt(p.T.label)]))
    follow('never mind', 'ignore (that|)', 'cancel',
        p => p.resolve(null))
    fallback('tell me the (name|title) of the (record|audio|track) (to play|)',
        'what is the (name|title) of the (record|audio|track) (to play|)')
})

intent(`(play|start) $(R record|audio|music|track|) $(T~ ${titles}|)`,
    async p => {
        let s = null;
        if (p.T.label) {
            s = tracks[parseInt(p.T.label)];
        } else {
            p.play(`What ${p.R} (should I play|)`, `what is the name of the ${p.R}`)
            s = await p.then(what)
        }
        if (s) {
            p.play(_.extend({embeddedPage:true, page: "r3hab_example.html", type:"audio"}, s));
            p.play(`Playing ${p.R} ${s.cleanTitle}`);
        } else {
            p.play('(sure|ok)')
        }
    });
