function show_person(p, name) {
    displayPage(p, people[name].IMG);
    var answer = people[name].name
    var mid
    if (people[name].title === "Associate Professor"){
        mid = "an Associate Professor "
    }
    else {
        mid = "a " + people[name].title
    }
    p.play(answer + ` is ` + mid + ` of the (CS department|CS|Department of computer science) faculty. (Heres more information|Here are more details)`, answer + `takes position of `+mid + ` of the (CS department|CS|Department of computer science) faculty. (Heres more information|Here are more details)`)
}

function resolve_name(p, name) {

    var result
    var i
    var buf
    var j
    var request
    if (name.split(" ").length==1) {
        if (NAMES_special_test.includes(name)) {
            var search_persons = []
            var first_name = name.charAt(0).toUpperCase() + name.substr(1)
            //possible_names_full = "$(T "
            //possible_names_small = "$(T "

            for (i = 0; i < NAMES_special[name].length; i++) {
                var last_name = NAMES_special[name][i].charAt(0).toUpperCase() + NAMES_special[name][i].substr(1)
                search_persons.push(people[first_name + "_" + last_name].name_res)
                //possible_names_full+=people[first_name + " " + last_name].name_res+"|"
                //possible_names_small+=people[last_name].name_res+"|"
            }
            //possible_names_full= possible_names_full.slice(0,possible_names_full.length-1)+")"
            //possible_names_small= possible_names_small.slice(0,possible_names_small.length-1)+")"
            request = ""
            for (i = 0; i < search_persons.length - 1; i++) {
                request += search_persons[i] + ", "
            }
            request = request.slice(0, request.length - 2) + " or " + search_persons[i] + "?"
            p.play("Please, (specify|clarify) whom did you mean:", "Did you talked about:")
            p.play(request)
            return -1
        }

        for (i = 0; i < NAMES_full_test.length; i++) {
            if (NAMES_full_test[i].split(" ")[0] === name) {
                result = ""
                buf = NAMES_full_test[i].split(" ");
                for (j = 0; j < buf.length; j++) {
                    result += buf[j].charAt(0).toUpperCase() + buf[j].substr(1) + "_"
                }
                result = result.slice(0, result.length - 1)
                return result
            }
        }
    }
    if (name.split(" ").length>=2) {
        for (i=0; i<NAMES_full_test.length;i++){
            if (NAMES_full_test[i]===name) {
                result = ""
                buf = NAMES_full_test[i].split(" ");
                for (j =0;j<buf.length;j++){
                    result+=buf[j].charAt(0).toUpperCase() + buf[j].substr(1)+"_"
                }
                result=result.slice(0, result.length-1)
                return result
            }}
    }
}

